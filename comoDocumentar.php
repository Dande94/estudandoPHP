<?php
/*
*
* Função media();
*
* Está função somar os valores $x e $y e dividir por $z para extrair a média;
*
* @package ControleDeEstoque //de qual projeto essa função faz parte
* @author Anderson Nunes <exemplo@exemplo.com> //quem é o autor dessa função e como entrar em contato 
*
* @param $x float é o primeiro valor a ser somado;
* @param $y float é o segundo valor a ser somado;
* @param $z int é o valor que servirá de divisor;
* @return float;
*/

function media($x,$y,$z){
    return ($x + $y)/$z;
}

?>